import { NumberToEnglish } from "../src/js/digit_to_english.js";

test("doNumberToEnglish()", () => {
    const numberToEnglishObj = new NumberToEnglish("-0");
    expect(numberToEnglishObj.doNumberToEnglish("-0")).toBe("-zero");
});


test("doNumberToEnglish()", () => {
    const numberToEnglishObj = new NumberToEnglish("999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999");
    expect(numberToEnglishObj.doNumberToEnglish("999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999")).toBe("");
});